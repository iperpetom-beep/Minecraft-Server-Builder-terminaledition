def expand(path: str) -> str:
    return os.path.expanduser(path)


from mcmanager.utils.java import JAVA17


def generate_run_sh(path: str, ram: str):
    script = (
        "#!/bin/bash\n"
        "cd \"$(dirname \"$0\")\"\n"
        f"\"{JAVA17}\" -Xms{ram} -Xmx{ram} -jar server.jar nogui\n"
    )
    dest = os.path.join(path, "run.sh")
    with open(dest, "w") as f:
        f.write(script)
    os.chmod(dest, 0o755)


def accept_eula(path: str):
    with open(os.path.join(path, "eula.txt"), "w") as f:
        f.write("eula=true\n")


